#Basic arithmetic operations in Python

#Add two numbers
print(2 + 4)
print(type(10 / 5))

#Exponentiation
print(2**3)

x = 5
y = x**2
print(y)

#String
a = "Hello, how're you?"  #equivalently: a = "Hello"

#Arithmetic operations on strings
#Multiplication
print(a * 5)

#Concatenation
b = "I'm good, thank you"
print(a + " " + b)

#Indexing and slicing
#First element
print([0])

#Len
print(f'Our string a has {len(a)} characters')

print('Our string a has {} characters'.format(len(a)))

#Logical statements
print(2 == 2)

print(2 == 3)  #False = equal to 0

print(True == 1)  #Checking whether True is equivalent to 1

#True evaluates to 1 and False evaluates to 0

print(True + True + True)

print(a == b)

#Multiple logical statements

print(2 == 2 and 3 == 3)

print(2 != 3)

print(2<3)
